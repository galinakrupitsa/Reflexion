public class Person {
    String name;
    int age;


    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }


    public void displayInfo() {
        System.out.println("Имя: " + name + ", возраст: " + age);
    }
    public void Kotik() {
        System.out.println("Котики милые");
    }
}
